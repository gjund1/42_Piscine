void	ft_rev_int_tab(int *tab, int size)
{
	int	swap;
	int	j;

	swap = 0;
	j = 0;
	while (j < (size / 2))
	{
		swap = tab[j];
		tab[j] = tab[size -1 - j];
		tab[size - 1 - j] = swap;
		j++;
	}
}
